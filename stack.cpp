#include <iostream>
#include "stack.h"

Stack::Stack() {
    top = NULL;
}

//==========================================================================================================================================================

Stack::~Stack() {
    while (top != NULL)
        pop();
}

//==========================================================================================================================================================

void Stack::clear() {
    while (top != NULL)
        pop();
}

//==========================================================================================================================================================

void Stack::push(int pushContent) {
    Node* tmp = new Node;
    if (tmp != NULL) {
        tmp->content = pushContent;
        tmp->next = top;
        top = tmp;
        tmp = NULL;

    }
}

//==========================================================================================================================================================

int Stack::pop() {
    if (top != NULL) {
        Node* tmp = top;
        top = top->next;
        int popItem = tmp->content;
        delete tmp;
        tmp = NULL;
        return popItem;
    }
    else
        return -1;
}

//==========================================================================================================================================================

bool Stack::isEmpty() {
    if (top == NULL)
        return true;
    else
        return false;

}

